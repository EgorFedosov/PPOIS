using Farm.Products;
using Farm.Places;

namespace Farm.Interfaces;

/// <summary>
/// Представляет базовый контракт для всех животных на ферме.
/// Определяет поведение, связанное с перемещением, взаимодействием с окружением,
/// отображением статистики и производством продуктов.
/// </summary>
public interface IAnimal
{
    /// <summary>
    /// Получает продукт, который может производить животное (например, молоко, яйца и т.д.).
    /// Может быть <see langword="null"/>, если животное не производит продукт.
    /// </summary>
    Product? Product { get; }

    /// <summary>
    /// Отображает текущую статистику животного: имя, возраст, здоровье, сытость и продуктивность.
    /// </summary>
    void PrintStats();

    /// <summary>
    /// Производит характерный звук, соответствующий типу животного.
    /// </summary>
    void MakeSound();

    /// <summary>
    /// Перемещает животное в указанное место на ферме.
    /// Если место совпадает с текущим или <see langword="null"/>, перемещение не происходит.
    /// </summary>
    /// <param name="newPlace">Новое место, куда следует переместить животное.</param>
    void MoveTo(Place? newPlace);
}